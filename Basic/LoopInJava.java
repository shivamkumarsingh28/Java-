public class LoopInJava {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }

        // dowhile 

        int I = 0;
        do {
            System.out.println(I);
            I++;
        }
        while (I<5);
    }
    
}
